# Simple loop through a range of numbers
for i in range(5):
    print(i)  # This will print numbers 0 through 4

# Loop through a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Loop with range starting at 1 and ending at 5
for num in range(1, 6):
    print(f"Number: {num}")